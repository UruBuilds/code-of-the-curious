// Naming as reverence
// ----------------------------------------------------------------
// üå± To begin coding is to begin naming -
//     declaring what deserves to be remembered, thus imprinting it into reality.
//     This is the soul of a variable.
// ----------------------------------------------------------------

// We use 'let' when we know that life, like a variable, changes.
let currentMood = 'hopeful';
//~ Observe: the words and letters we choose to commit to memory are held within quotation marks.
//~ Even numbers ‚Äî once wrapped in these marks ‚Äî are transformed.
//~ They become known as `strings`, no longer quantities, but symbols with meaning.

console.log('Mood:', currentMood); //hopeful

// The variable shifts - as we shift.
currentMood = 'resolute';
console.log('Mood:', currentMood); //resolute

// ----------------------------------------------------------
// üîí Some things are not meant to change ‚Äî they are constants.
//     Use 'const' to protect sacred values.
// ----------------------------------------------------------

const birthName = 'Uru Onyemaobi';
console.log(birthName); // Uru Onyemaobi

// Try changing it - JavaScript will resist.
// birthName = "Zikora"; //‚ùå TypeError: Assignment to constant variable.

// ----------------------------------------------------------
// üß† The act of naming must be intentional.
//     A variable name is not for the machine, or for the console ‚Äî it's for the mind.
// ----------------------------------------------------------

let purpose = 'to learn deeply';
let companion = 'curiosity';

console.log('My purpose is', purpose);
console.log('My companion is', companion);

// ----------------------------------------------------------
// üîÑ Memory can be updated, overwritten ‚Äî like a journal.
//     But not everything should be mutable.
// ----------------------------------------------------------

let guidingWord = 'integrity';
console.log("Today's guiding essence:", guidingWord);

guidingWord = 'courage';
console.log("Tomorrow's guiding essence:", guidingWord);

// ----------------------------------------------------------
// üåå Ask yourself, every single time you build:
//    What am I choosing to remember?
//    What deserves to be remain unchanged?
//    What names do I give to the truths I carry?

